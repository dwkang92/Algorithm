# Python은 4가지의 scala 타입이 있습니다. (정수 int, 실수 float, 값없음 None, 참/거짓의 Boolean)
# 개인적으로 boolean type에 이해가 부족하였으므로, 이해하기 쉽게 boolean type을 출력해보는 연습을 해보겠습니다.

bool(0)  # False
bool(1)  # True
bool(0.0)  # float type을 boolean으로 만들어 보았습니다. 0.0만 False이고 나머지는 True가 됩니다.
bool(-1.5)  # True
bool("")  # False. 문자형을 bool형으로 변환하였습니다. empty value만이 false인 것을 확인 할 수 있습니다.

