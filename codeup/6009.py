# 문자열을 실수로 변환하기 위해서는 float() 함수를 사용한다.
# 숫자를 문자열로 변환하기 위해서는 str() 함수를 사용한다.
# print 함수를 사용한 문자열 연결시 콤마(,)를 사용하면 숫자가 자동으로 문자열로 변경되지만 더하기 기호(+)로 연결을 할 때는 오류가 나기 때문에 반드시 형변환을 해야한다.
