# 숫자를 입력받아 변수에 지정하였다. input 함수는 사용자가 숫자를 입력하더라도 "문자열"로 입력을 받는다.
# 그렇기 때문에 int함수를 사용하여 바로 정수로 변환. 두번째 숫자는 문자열 그대로 두었다. 왜냐면, 곱셉에서 두번째 숫자는 하나씩 계산되어지기 때문이다.
# 문자열은 split함수를 이용하든 인덱스로 문자의 위치를 이용하든, 하나씩 분리를 할 수가 있지만, 숫자는 그렇게 할 수가 없기 때문이다.

n1 = int(input())
n2 = input()

print(n1 * int(n2[2]))  # 1의 자리 숫자
print(n1 * int(n2[1]))  # 10의 자리 숫자
print(n1 * int(n2[0]))  # 100의 자리 숫자
print(n1 * int(n2))  # 계산식
